const a = 7
let b = 3

//atribuição aditiva - acrescentando o valor de 'a' na var 'b' B = B + A
b += a
console.log(b)

//atribuição subtrativa - subtraindo o valor de 4 na var 'b' B = B - 4
b -= 4
console.log(b)

//atribuição multiplicativa - multiplicando o valor de 2 na var 'b' B = B * 2
b *= 2
console.log(b)

//atribuição divisível - dividindo o valor de 'b' por 2 B = B / 2
b /= 2
console.log(b)

//atribuição modular - o resto da operação B = B % 2 Se par = 0, se ímpar = 1
b %= 2
console.log(b)

