let num1 = 1
let num2 = 2

//pós fixada - acrescenta uma unidade ao valor da var
num1++
console.log(num1)

//pré fixada - prioridade maior
--num1
console.log(num1)

//true
console.log(++num1 === num2--)
//false
console.log(num1 === num2)

